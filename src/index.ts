/**
 * add two numbers 
 * @param a number
 * @param b number
 * @returns result
 */

export const add =(a:number,b:number):number=>a + b;
/**
 * substract two numbers 
 * @param a number
 * @param b number
 * @returns result
 */

export const substract =(a:number,b:number):number=>a - b;
/**
 * multiply two numbers 
 * @param a number
 * @param b number
 * @returns result
 */

export const multiply=(a:number,b:number):number=>a * b;

